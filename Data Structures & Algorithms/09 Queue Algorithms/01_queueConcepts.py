'''
- Queue: A queue is a fundamental data structure in computer science that follows the First-In-First-Out (FIFO) principle.
       In a queue, the first element added is the first to be removed, just like people waiting in a queue or line. Items
       are added to the rear (enqueue) and removed from the front (dequeue) of the queue.

- Key characteristics of a queue:
  a. enqueue (Insertion): This operation adds an element to the rear of the queue.
  b. dequeue (Removal): This operation removes the element from the front of the queue.
  c. front: The front of the queue is where elements are dequeued.
  d. rear (Back): The rear (or back) of the queue is where elements are enqueued.
  e. size: The size of the queue represents the number of elements in the queue.

- Common uses of queues include:
  a. Task Scheduling: Queues are used to manage tasks in a "first-come, first-served" manner, such as in print job queues
     or task queues in multi-threaded systems.
  b. Breadth-First Search: In graph algorithms, queues are used to implement breadth-first search (BFS), where nodes are visited level by level.
  c. Resource Allocation: Queues are used to allocate resources to processes, such as CPU scheduling in operating systems.
  d. Buffering: In data communication and I/O systems, queues are used to buffer and manage data as it is transferred between two systems or devices.
'''

