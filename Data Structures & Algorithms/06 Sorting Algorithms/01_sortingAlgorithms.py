'''
    - Sorting Algorithms:
       - Sorting Algorithms is used to sort data in ascending or descending order, which is helpful in a wide range of
         applications, from databases to search algorithms.

       - Types of Sorting Algorithms:
         a. Selection Sort: Time Complexity- O(n^2)
            - It repeatedly selects the smallest (or largest) element from the unsorted region and moves it to the sorted
              region. Its time complexity is O(n^2).

         b. Insertion Sort: Time Complexity- O(n^2)
            - It builds a sorted array by repeatedly taking elements from the unsorted part and inserting them into their
              correct positions in the sorted part. It's efficient for small datasets.

         c. Bubble Sort: Time Complexity- O(n^2)
            - It is a simple comparison-based algorithm that repeatedly steps through the list, compares adjacent elements,
              and swaps them if they are in the wrong order.

         d. Merge Sort: Time Complexity- O(n log n)
            - It is a divide-and-conquer algorithm that divides the input into smaller sub-problems, sorts them, and then
              merges the sorted sub-lists. It's efficient and stable sorting algorithms.

         e. Quick Sort: Time Complexity- O(n log n)
            - It is a divide-and-conquer algorithm as well. It selects a 'pivot' element from the array and partitions
              the other elements into two sub-arrays according to whether they are less than or greater than the pivot.
              The sub-arrays are then sorted recursively.
'''